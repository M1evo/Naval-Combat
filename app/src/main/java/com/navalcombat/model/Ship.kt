package com.navalcombat.model

/**
 * Ориентация корабля на игровом поле.
 *
 * Определяет направление, в котором корабль «растёт» от начальной клетки:
 * - [HORIZONTAL] — вправо (увеличение столбца);
 * - [VERTICAL]   — вниз  (увеличение строки).
 */
enum class Orientation {
    /** Горизонтальное расположение: палубы идут слева направо. */
    HORIZONTAL,

    /** Вертикальное расположение: палубы идут сверху вниз. */
    VERTICAL
}

/**
 * Корабль, размещённый на [Board].
 *
 * Каждый корабль определяется начальной клеткой [startCell], длиной [size]
 * и [orientation]. Набор палуб вычисляется динамически методом [getCells].
 * Внутри корабля ведётся учёт попаданий ([hitCells]), позволяющий определить,
 * потоплен ли он ([isSunk]).
 *
 * Класс реализован как `data class`, чтобы корректно работать
 * при сравнении и удалении из коллекций в [Board].
 *
 * @property startCell Клетка первой (верхней-левой) палубы корабля.
 * @property size      Количество палуб (длина). Допустимые значения: 1–4.
 * @property orientation Направление размещения относительно [startCell].
 *
 * @see Board.placeShip
 */
data class Ship(
    val startCell: Cell,
    val size: Int,
    val orientation: Orientation
) {
    /**
     * Множество клеток корабля, в которые уже зафиксированы попадания.
     *
     * Заполняется через [hit]; используется в [isSunk] и [isHit].
     */
    private val hitCells: MutableSet<Cell> = mutableSetOf()

    /**
     * Вычисляет список клеток, занимаемых кораблём, в порядке от [startCell].
     *
     * Для горизонтального корабля длиной 3 с началом в `(2, 4)` результат будет
     * `[(2,4), (2,5), (2,6)]`. Список создаётся заново при каждом вызове,
     * поэтому его можно безопасно модифицировать на стороне вызывающего кода.
     *
     * @return Список [Cell], составляющих палубы корабля.
     */
    fun getCells(): List<Cell> {
        return (0 until size).map { i ->
            when (orientation) {
                Orientation.HORIZONTAL -> Cell(startCell.row, startCell.col + i)
                Orientation.VERTICAL -> Cell(startCell.row + i, startCell.col)
            }
        }
    }

    /**
     * Регистрирует попадание по указанной клетке корабля.
     *
     * Если [cell] действительно принадлежит кораблю, она добавляется
     * во внутреннее множество [hitCells]. Повторный вызов с той же клеткой
     * не вызовет ошибки, но вернёт `true`, поскольку клетка входит в корабль.
     *
     * @param cell Клетка, в которую произведён выстрел.
     * @return `true`, если [cell] принадлежит данному кораблю; `false` — иначе.
     */
    fun hit(cell: Cell): Boolean {
        return if (cell in getCells()) {
            hitCells.add(cell)
            true
        } else {
            false
        }
    }

    /**
     * Проверяет, потоплен ли корабль.
     *
     * Корабль считается потопленным, когда количество уникальных попаданий
     * равно числу его палуб ([size]).
     *
     * @return `true`, если все палубы поражены.
     */
    fun isSunk(): Boolean = hitCells.size == size

    /**
     * Проверяет, было ли уже засчитано попадание в указанную клетку.
     *
     * @param cell Проверяемая клетка.
     * @return `true`, если по [cell] ранее было зафиксировано попадание.
     */
    fun isHit(cell: Cell): Boolean = cell in hitCells

    /**
     * Возвращает неизменяемую копию множества поражённых клеток.
     *
     * @return [Set] клеток, в которые уже попали.
     */
    fun getHitCells(): Set<Cell> = hitCells.toSet()
}
