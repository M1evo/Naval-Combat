package com.navalcombat.model

/**
 * Координата одной клетки на игровом поле размером [BOARD_SIZE]×[BOARD_SIZE].
 *
 * Клетка задаётся парой целочисленных индексов (строка, столбец),
 * нумерация начинается с нуля. Экземпляры используются повсеместно —
 * от размещения кораблей до регистрации выстрелов — поэтому класс
 * реализован как `data class` для корректного сравнения и хранения в коллекциях.
 *
 * @property row Индекс строки (0-based). Диапазон: `0` до `BOARD_SIZE - 1`.
 * @property col Индекс столбца (0-based). Диапазон: `0` до `BOARD_SIZE - 1`.
 */
data class Cell(val row: Int, val col: Int) {

    /**
     * Проверяет, попадает ли клетка в границы игрового поля.
     *
     * Метод используется при валидации размещения кораблей,
     * обработке выстрелов и расчёте соседних клеток.
     *
     * @return `true`, если и [row], и [col] находятся в `[0, BOARD_SIZE)`.
     */
    fun isValid(): Boolean = row in 0 until BOARD_SIZE && col in 0 until BOARD_SIZE

    companion object {
        /**
         * Размер стороны квадратного игрового поля.
         *
         * По правилам классического «Морского боя» поле имеет размер 10×10.
         */
        const val BOARD_SIZE = 10
    }
}

/**
 * Визуальное состояние клетки на поле **соперника**.
 *
 * Используется при отрисовке поля противника в [BoardView],
 * где игрок не видит расстановку кораблей — только результаты
 * уже совершённых выстрелов.
 */
enum class CellState {
    /** Клетка ещё не была атакована — внешний вид по умолчанию (вода). */
    UNKNOWN,

    /** Выстрел пришёлся в пустую клетку — отображается точкой-маркером промаха. */
    MISS,

    /** Выстрел попал в палубу корабля — отображается крестиком-маркером попадания. */
    HIT
}
